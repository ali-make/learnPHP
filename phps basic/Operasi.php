<?php 
/**
 * Operator dipakai untuk menjalankan operasi matematika pada variabel
 * Operator dalam PHP dibagi seperti berikut:
 * - Aritmetika operator
 * - Assignment operator
 * - Comparison operator
 * - Increment/Decrement operator
 * - Logical operator
 * - String operator
 * - Array operator
 * - Conditional assignment operator
 */

 /**
  * Aritmatika Operator
  */
  $a = 4;
  $b = 5;
  echo "<h2>";

  echo $a + $b . "<br>";
  echo $a - $b . "<br>";
  echo $a * $b . "<br>";
  echo $a / $b . "<br>";
  echo $a % $b . "<br>";
  echo $a ** $b . "<br>";

  echo "</h2>";

  echo "<h2>Berhenti sampai sini!</h2>";